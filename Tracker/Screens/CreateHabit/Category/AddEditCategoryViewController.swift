import UIKit

final class AddEditCategoryViewController: UIViewController {
    
    enum Mode {
        case create
        case edit(String) // содержит старое название категории
    }
    
    // MARK: - Properties
    private let mode: Mode
    var onCategoryCreated: ((String) -> Void)?
    var onCategoryUpdated: ((String, String) -> Void)?
    
    // MARK: - UI Elements
    private lazy var textField: UITextField = {
        let textField = UITextField()
        textField.placeholder = "Введите название категории"
        textField.layer.cornerRadius = 17
        textField.backgroundColor = UIColor(resource: .yLightGray)
        textField.translatesAutoresizingMaskIntoConstraints = false
        
        let leftPaddingView = UIView(frame: CGRect(x: 0, y: 0, width: 16, height: textField.frame.height))
        textField.leftView = leftPaddingView
        textField.leftViewMode = .always
        
        textField.addAction(UIAction { [weak self] _ in
            self?.updateDoneButtonState()
        }, for: .editingChanged)
        
        return textField
    }()
    
    private lazy var doneButton: UIButton = {
        let button = UIButton(type: .system)
        button.setTitle("Готово", for: .normal)
        button.backgroundColor = .yGray
        button.setTitleColor(.white, for: .normal)
        button.titleLabel?.font = UIFont.systemFont(ofSize: 16, weight: .medium)
        button.layer.cornerRadius = 16
        button.isEnabled = false
        button.translatesAutoresizingMaskIntoConstraints = false
        
        button.addAction(UIAction { [weak self] _ in
            self?.doneButtonTapped()
        }, for: .touchUpInside)
        
        return button
        
    }()
    
    // MARK: - Initialization
    init(mode: Mode) {
        self.mode = mode
        super.init(nibName: nil, bundle: nil)
    }
    
    required init?(coder: NSCoder) {
        fatalError("init(coder:) has not been implemented")
    }
    
    // MARK: - Lifecycle
    override func viewDidLoad() {
        super.viewDidLoad()
        setupUI()
        setupNavigationBar()
        configureForMode()
        textField.becomeFirstResponder()
    }
    
    // MARK: - Setup
    private func setupUI() {
        view.backgroundColor = .white
        view.addSubview(textField)
        view.addSubview(doneButton)
        
        NSLayoutConstraint.activate([
            textField.topAnchor.constraint(equalTo: view.safeAreaLayoutGuide.topAnchor, constant: 24),
            textField.leadingAnchor.constraint(equalTo: view.leadingAnchor, constant: 16),
            textField.trailingAnchor.constraint(equalTo: view.trailingAnchor, constant: -16),
            textField.heightAnchor.constraint(equalToConstant: 75),
            
            doneButton.leadingAnchor.constraint(equalTo: view.leadingAnchor, constant: 20),
            doneButton.trailingAnchor.constraint(equalTo: view.trailingAnchor, constant: -20),
            doneButton.bottomAnchor.constraint(equalTo: view.safeAreaLayoutGuide.bottomAnchor, constant: -16),
            doneButton.heightAnchor.constraint(equalToConstant: 60)
        ])
    }
    
    private func setupNavigationBar() {
        navigationItem.title = {
            switch mode {
            case .create:
                return "Новая категория"
            case .edit:
                return "Редактирование категории"
            }
        }()
        
        navigationItem.leftBarButtonItem = nil
        navigationItem.rightBarButtonItem = nil
    }
    
    private func configureForMode() {
        if case .edit(let oldTitle) = mode {
            textField.text = oldTitle
            updateDoneButtonState()
        }
    }
    
    private func updateDoneButtonState() {
        let text = textField.text?.trimmingCharacters(in: .whitespacesAndNewlines) ?? ""
        let isEmpty = text.isEmpty
        
        doneButton.isEnabled = !isEmpty
        doneButton.backgroundColor = isEmpty ? .yGray : .yBlackDay
    }
    
    // MARK: - Actions
    @objc private func doneButtonTapped() {
        guard let text = textField.text?.trimmingCharacters(in: .whitespacesAndNewlines),
              !text.isEmpty else {
            return
        }
        
        switch mode {
        case .create:
            onCategoryCreated?(text)
        case .edit(let oldTitle):
            onCategoryUpdated?(oldTitle, text)
        }
        
        dismiss(animated: true)
    }
}
